# 정수 두개 입력받고 계산하는 프로그램 작성

# 첫 번째수 인트로 입력받기
# "첫 번째 수: "
n1 = int(input("첫 번째 수: "))
# 두 번째수 인트로 입력받기
# "두 번째 수: "
n2 = int(input("두 번째 수: "))


# "더하기: " n1 + n2
print("더하기: ",(n1 + n2))
# "빼기: " n1 - n2
print("빼기: ",(n1 - n2))
# "곱하기: " n1 * n2
print("곱하기: ",(n1 * n2))
# "나누기: " n1 / n2
print("나누기: ",(n1 / n2))
# "몫: " n1 // n2
print("몫: ",(n1 // n2))
# "나머지: "  n1 % n2
print("나머지: ",(n1 % n2))
# "거듭제곱: " n1 ** n2
print("거듭제곱: ",(n1 ** n2))