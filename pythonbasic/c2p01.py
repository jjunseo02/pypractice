# 자기소개 변수 만들기

"""
지시사항
1. 본인 이름 저장변수 name으로 선언
2. 본인 나이 저장변수 age로 선언
3. 본인 학과 저장변수 major로 선언
4. 각변수 프린트하기
"""

# 본인 이름 name으로 받기 "이름을 입력해주세요: "
name = input("이름을 입력해주세요: ")

# 본인 나이 정수 age 로 받기 "나이를 입력해주세요: "
age = int(input("나이를 입력해주세요: "))

# 본인 학과 major로 받기 "학과를 입력해주세요: "
major = input("학과를 입력해주세요: ")

# 출력하기
print(name)
print(age)
print(major)